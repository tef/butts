package butts.prototype;

import butts.Prototype;

public final class  WrappedPrototypeException extends RuntimeException {
	private static final long serialVersionUID = -427016579283436293L;
	private final Prototype p;

	public WrappedPrototypeException(Prototype p) {
		this.p = p;
	}

	public Prototype getPrototype() {
		return p;
	}

}